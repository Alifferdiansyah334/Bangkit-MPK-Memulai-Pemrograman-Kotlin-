/*
    If Expression Sebagai Statements
*/

fun main(){

    val now:Int = 10
    val openOffice:Int = 9

    if(now > openOffice){
        print("Office Already Open")
    }
    else{
        print("Office close")
    }

    /*
        Pada contoh kode diatas, if dikatakan sebagai statement
        karena tidak mengembalikan nilai apapun hanya sebagai percabangan
        pada bahasa pemrograman kotlin
    */
} 