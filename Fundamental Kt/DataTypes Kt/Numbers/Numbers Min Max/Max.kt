/*
    Penggunaan MAX_VALUE untuk mengetahui maksimum nilai yang dapat ditampung pada tipedata
*/

fun main(){
    val maxInteger = Int.MAX_VALUE
    val maxLong = Long.MAX_VALUE
    val maxShort = Short.MAX_VALUE
    val maxByte = Byte.MAX_VALUE
    val maxDouble = Double.MAX_VALUE
    val maxFloat = Float.MAX_VALUE

    println(maxInteger)
    println(maxLong)
    println(maxShort)
    println(maxByte)
    println(maxDouble)
    println(maxFloat)
}