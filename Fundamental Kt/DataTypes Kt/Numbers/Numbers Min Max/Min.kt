/*
    Penggunaan MAX_VALUE untuk mengetahui maksimum nilai yang dapat ditampung pada tipedata
*/

fun main(){
    val minInteger = Int.MIN_VALUE
    val minLong = Long.MIN_VALUE
    val minShort = Short.MIN_VALUE
    val minByte = Byte.MIN_VALUE
    val minDouble = Double.MIN_VALUE
    val minFloat = Float.MIN_VALUE

    println(minInteger)
    println(minLong)
    println(minShort)
    println(minByte)
    println(minDouble)
    println(minFloat)
}